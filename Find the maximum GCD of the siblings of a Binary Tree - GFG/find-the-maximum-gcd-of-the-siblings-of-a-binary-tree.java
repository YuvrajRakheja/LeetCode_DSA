class Solution {
    int gcd=0;
    int ans;
    
    int maxGCD(Node root) {
        // code here
        ans=0;
        calcgcd(root);
        return ans;
    }
    public void calcgcd(Node root){
        if(root==null)  return;
        
        if(root.left!=null && root.right!=null){
            int gcdofnum=gcd(root.left.data,root.right.data);
            if(gcdofnum>gcd){
                ans=root.data;
                gcd=gcdofnum;
            }else if(gcdofnum==gcd){
                ans=Math.max(ans,root.data);
            }
        }
        calcgcd(root.left);
        calcgcd(root.right);
    }
    public int gcd(int n1,int n2){
        while(n1%n2!=0){
            int rem=n1%n2;
            n1=n2;
            n2=rem;
        }
        return n2;
    }
}

